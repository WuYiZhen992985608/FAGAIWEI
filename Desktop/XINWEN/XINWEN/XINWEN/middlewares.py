class MyRetryMiddleware(RetryMiddleware):
    logger = logging.getLogger(__name__)

    def delete_proxy(self, proxy):
        if proxy:
            # delete proxy from proxies pool
            pass


    def process_response(self, request, response, spider):
        if request.meta.get('dont_retry', False):
            return response
        if response.status in self.retry_http_codes:
            reason = response_status_message(response.status)
            # 删除该代理
            self.delete_proxy(request.meta.get('proxy', False))
            time.sleep(random.randint(3, 5))
            self.logger.warning('返回值异常, 进行重试...')
            return self._retry(request, reason, spider) or response
        return response


    def process_exception(self, request, exception, spider):
        if isinstance(exception, self.EXCEPTIONS_TO_RETRY) \
                and not request.meta.get('dont_retry', False):
            # 删除该代理
            self.delete_proxy(request.meta.get('proxy', False))
            time.sleep(random.randint(3, 5))
            self.logger.warning('连接异常, 进行重试...')

            return self._retry(request, exception, spider)


class XINWEN_DeduplicateMiddleware(object):
    @classmethod
    def from_crawler(cls, crawler):
        return cls(crawler.settings)

    #注意 看看表中字段 新闻 可以看爬虫名字 和对应已经爬取够的链接
    def __init__(self, settings):
        #加载已经爬取过的Url - Key
        self.sqlcount = 'select count(*) from topic_info_government_policy where spider_name ="XINWEN"'
        # self.sqlcount = 'select count(*) from topic_info_government_policy where spider_name ="beijing"'
        self.sqlselect = 'select `link` from topic_info_government_policy where spider_name ="XINWEN"'
        # self.sqlselect = 'select `link` from topic_info_government_policy where spider_name ="beijing"'
        self.filter = DataBaseFilter(settings, self)

    def process_request(self, request, spider):
        return self.filter.process_request(request, spider)

    #需根据具体url具体分析
    #比如
    #http://www.xinjiang.gov.cn/xinjiang/xjyw/202011/0406fbefcd7f4b29bc26e8426fd8137e.shtml
    #发现所有新闻页面前缀都有http://www.xinjiang.gov.cn/xinjiang/xjyw/
    #我们以后半部分来做key进行过滤是否已经爬取过了
    #这么做是为了节省内存 使用最小量的key，当然也可以直接使用完整链接进行过滤
    def get_url_key(self, url):
        lstNodes = url.split('http://www.xinjiang.gov.cn/xinjiang/xjyw/')
        # lstNodes = url.split('http://fgw.beijing.gov.cn')
        if len(lstNodes) != 2:
            return ''
        url = lstNodes[1]
        if not url.endswith('.shtml'):
        # if not url.endswith('.htm'):
        # if not url.endswith('.html'):
            return ''
        key = url.replace('.shtml', '')
        # key = url.replace('.htm', '')
        # key = url.replace('.html', '')
        return key

    #需要过滤的页面 -列表页是不需过滤的
    #此需要根据网站情况观察url来进行判定
    #我们只需要过滤具体的新闻内容页面 -一旦爬过了下次不再爬取了
    def is_filter_url(self, url):
        return not url.startswith('http://www.xinjiang.gov.cn/xinjiang/xjyw/common_list')
